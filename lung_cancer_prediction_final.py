# 2. DEFINE DATASET PATHS
# =========================================================
# Dataset must be structured as:
# dataset/train, dataset/valid, dataset/test

train_folder = './dataset/train'
valid_folder = './dataset/valid'
test_folder  = './dataset/test'

# =========================================================
# 🔹 3. IMPORT LIBRARIES
# =========================================================

import numpy as np
import tensorflow as tf
import matplotlib.pyplot as plt

from tensorflow.keras.preprocessing.image import ImageDataGenerator
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, GlobalAveragePooling2D, Dropout
from tensorflow.keras.callbacks import EarlyStopping, ReduceLROnPlateau

from sklearn.metrics import classification_report, confusion_matrix

# =========================================================
# 🔹 4. IMAGE PREPROCESSING
# =========================================================
# All images resized and normalized

IMAGE_SIZE = (224, 224)
BATCH_SIZE = 16

# Data augmentation to improve generalization
train_datagen = ImageDataGenerator(
    rescale=1./255,             # Normalization (0–255 → 0–1)
    horizontal_flip=True,       # Flip images
    rotation_range=20,          # Rotate images
    zoom_range=0.2              # Zoom images
)

# Validation data only normalized
valid_datagen = ImageDataGenerator(rescale=1./255)

# =========================================================
# 🔹 5. LOAD DATASET
# =========================================================

train_generator = train_datagen.flow_from_directory(
    train_folder,
    target_size=IMAGE_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical'
)

valid_generator = valid_datagen.flow_from_directory(
    valid_folder,
    target_size=IMAGE_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical'
)

print("Class Labels:", train_generator.class_indices)

# =========================================================
# 🔹 6. MODEL (TRANSFER LEARNING - XCEPTION)
# =========================================================
# Using pretrained CNN model

base_model = tf.keras.applications.Xception(
    weights='imagenet',
    include_top=False,
    input_shape=(224, 224, 3)
)

# Freeze most layers to avoid overfitting
for layer in base_model.layers[:-30]:
    layer.trainable = False

# =========================================================
# 🔹 7. BUILD FINAL MODEL
# =========================================================

model = Sequential([
    base_model,                      # Feature extractor
    GlobalAveragePooling2D(),        # Reduce dimensions
    Dropout(0.5),                    # Prevent overfitting
    Dense(4, activation='softmax')   # 4 output classes
])

# =========================================================
# 🔹 8. COMPILE MODEL
# =========================================================

model.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.0001),
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

model.summary()

# =========================================================
# 🔹 9. CALLBACKS (OPTIMIZATION)
# =========================================================

early_stop = EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True
)

lr_reduce = ReduceLROnPlateau(
    monitor='val_loss',
    patience=3,
    factor=0.3
)

# =========================================================
# 🔹 10. TRAIN MODEL
# =========================================================

history = model.fit(
    train_generator,
    validation_data=valid_generator,
    epochs=20,
    callbacks=[early_stop, lr_reduce]
)

# =========================================================
# 🔹 11. FINAL ACCURACY
# =========================================================

print("Training Accuracy:", history.history['accuracy'][-1])
print("Validation Accuracy:", history.history['val_accuracy'][-1])

# =========================================================
# 🔹 12. PLOT RESULTS
# =========================================================

plt.figure(figsize=(12,5))

# Accuracy graph
plt.subplot(1,2,1)
plt.plot(history.history['accuracy'])
plt.plot(history.history['val_accuracy'])
plt.title('Accuracy')
plt.legend(['Train', 'Validation'])

# Loss graph
plt.subplot(1,2,2)
plt.plot(history.history['loss'])
plt.plot(history.history['val_loss'])
plt.title('Loss')
plt.legend(['Train', 'Validation'])

plt.show()

# =========================================================
# 🔹 13. SAVE MODEL
# =========================================================

model.save('lung_cancer_model_final.h5')

# Predict validation data
Y_pred = model.predict(valid_generator)
y_pred = np.argmax(Y_pred, axis=1)

# Classification Report (Precision, Recall, F1)
print("\nClassification Report:")
print(classification_report(valid_generator.classes, y_pred))

# Confusion Matrix
print("\nConfusion Matrix:")
print(confusion_matrix(valid_generator.classes, y_pred))

from tensorflow.keras.preprocessing import image
import matplotlib.pyplot as plt
